#!/usr/bin/env python3
"""Run the internal eval over the seeded-issue corpus and write results.

--subset smoke  runs only the smoke issues (fast, zero-token).
--subset full   runs the whole corpus.
--resume        skips issues already present in the results file, so a run
                interrupted by a free-tier rate limit can be re-invoked and
                pick up where it left off.

    python3 eval/runners/run_internal_eval.py --provider mock --subset smoke
"""

from __future__ import annotations

import argparse
import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "runners"))

from run_single_issue import load_issues, run_one  # noqa: E402
from metrics.pass_rate import pass_rate  # noqa: E402

RESULTS = ROOT / "results" / "internal-30-issues.json"
SCORECARD = ROOT / "results" / "final-scorecard.md"


def _select(subset: str) -> list[dict]:
    issues = load_issues()
    if subset == "smoke":
        return [i for i in issues if i.get("smoke")]
    return issues


def _load_prior(path: Path) -> dict:
    if path.exists():
        try:
            return json.loads(path.read_text())
        except json.JSONDecodeError:
            return {}
    return {}


def aggregate(results: list[dict]) -> dict:
    def avg(key: str) -> float:
        vals = [r["metrics"][key] for r in results if isinstance(r["metrics"].get(key), (int, float))]
        return round(sum(vals) / len(vals), 4) if vals else 0.0

    return {
        "count": len(results),
        "passRate": pass_rate(results),
        "avgPrQuality": avg("prQuality"),
        "avgStyleConformance": avg("styleConformance"),
        "avgSafetyScore": avg("safetyScore"),
        "avgOperatorUxScore": avg("operatorUxScore"),
        "totalDollars": round(sum(r["metrics"]["costLatency"]["dollars"] for r in results), 6),
        "totalTokens": sum(r["metrics"]["costLatency"]["tokens"] for r in results),
    }


def write_scorecard(summary: dict, provider: str, subset: str) -> None:
    lines = [
        "# Internal eval scorecard",
        "",
        f"- **Provider:** {provider}",
        f"- **Subset:** {subset}",
        f"- **Issues:** {summary['count']}",
        f"- **Pass rate:** {summary['passRate'] * 100:.1f}%",
        f"- **Avg PR quality:** {summary['avgPrQuality']}",
        f"- **Avg style conformance:** {summary['avgStyleConformance']}",
        f"- **Avg safety score:** {summary['avgSafetyScore']}",
        f"- **Avg operator-UX score:** {summary['avgOperatorUxScore']}",
        f"- **Total cost:** ${summary['totalDollars']} ({summary['totalTokens']} tokens)",
        "",
        "_Generated by `run_internal_eval.py`._",
        "",
    ]
    SCORECARD.write_text("\n".join(lines))


def main() -> int:
    ap = argparse.ArgumentParser(description="Run the internal seeded-issue eval.")
    ap.add_argument("--provider", default="mock")
    ap.add_argument("--subset", choices=["smoke", "full"], default="full")
    ap.add_argument("--resume", action="store_true")
    args = ap.parse_args()

    selected = _select(args.subset)
    done: dict[str, dict] = {}
    if args.resume:
        prior = _load_prior(RESULTS)
        done = {r["id"]: r for r in prior.get("results", []) if r.get("provider") == args.provider}
        if done:
            print(f"resume: {len(done)} prior result(s) reused")

    results: list[dict] = []
    for issue in selected:
        if issue["id"] in done:
            results.append(done[issue["id"]])
            continue
        results.append(run_one(issue["id"], args.provider))
        print(f"  ran {issue['id']}")

    summary = aggregate(results)
    RESULTS.parent.mkdir(parents=True, exist_ok=True)
    RESULTS.write_text(json.dumps(
        {"provider": args.provider, "subset": args.subset, "summary": summary, "results": results},
        indent=2) + "\n")
    write_scorecard(summary, args.provider, args.subset)
    print(f"wrote {RESULTS.relative_to(ROOT.parent)} and {SCORECARD.relative_to(ROOT.parent)}")
    print(f"pass rate: {summary['passRate'] * 100:.1f}%  ({summary['count']} issues, ${summary['totalDollars']})")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
